def range_in_list(the_list, index_start=0, index_stop=None):
    if index_stop is None:
        index_stop = len(the_list)
    print(sum(the_list[index_start: index_stop + 1]))



range_in_list([1,2,3,4],0,2) #  6
range_in_list([1,2,3,4],0,3) # 10
range_in_list([1,2,3,4],1) #  9
range_in_list([1,2,3,4]) # 10
range_in_list([1,2,3,4],0,100) # 10
range_in_list([],0,1) # 0

